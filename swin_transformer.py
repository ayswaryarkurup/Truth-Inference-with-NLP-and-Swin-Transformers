    "#Swin Transformer implementation adapted for the TIA model. This implementation includes the shifted window mechanism and Swish activation.\n",
    "\n",
    "import torch\n",
    "import torch.nn as nn\n",
    "import torch.nn.functional as F\n",
    "import torch.utils.checkpoint as checkpoint\n",
