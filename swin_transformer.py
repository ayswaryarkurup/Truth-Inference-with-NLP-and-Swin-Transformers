    "class Swish(nn.Module):\n",
    "    \"\"\"\n",
    "    Swish activation function: x * sigmoid(x)\n",
    "    \"\"\"\n",
    "    def forward(self, x):\n",
    "        return x * torch.sigmoid(x)\n",
    "\n",
    "class Mlp(nn.Module):\n",
    "    \n",
    "    def __init__(self, in_features, hidden_features=None, out_features=None, act_layer=nn.GELU, drop=0., use_swish=False):\n",
    "        super().__init__()\n",
    "        out_features = out_features or in_features\n",
    "        hidden_features = hidden_features or in_features\n",
    "        self.fc1 = nn.Linear(in_features, hidden_features)\n",
    "        self.act = Swish() if use_swish else act_layer()\n",
    "        self.fc2 = nn.Linear(hidden_features, out_features)\n",
    "        self.drop = nn.Dropout(drop)\n",
    "\n",
    "    def forward(self, x):\n",
    "        x = self.fc1(x)\n",
    "        x = self.act(x)\n",
    "        x = self.drop(x)\n",
    "        x = self.fc2(x)\n",
    "        x = self.drop(x)\n",
    "        return x\n",
    "\n",
    "def window_partition(x, window_size):\n",
    "    \"\"\"\n",
    "    Partition into non-overlapping windows with padding if needed\n",
    "    Args:\n",
    "        x: (B, H, W, C)\n",
    "        window_size: window size\n",
    "    Returns:\n",
    "        windows: (num_windows*B, window_size, window_size, C)\n",
    "    \"\"\"\n",
    "    B, H, W, C = x.shape\n",
    "    \n",
    "    \n",
    "    pad_h = (window_size - H % window_size) % window_size\n",
    "    pad_w = (window_size - W % window_size) % window_size\n",
    "    if pad_h > 0 or pad_w > 0:\n",
    "        x = F.pad(x, (0, 0, 0, pad_w, 0, pad_h))\n",
    "    \n",
    "    # Updated H, W after padding\n",
    "    H_pad, W_pad = H + pad_h, W + pad_w\n",
    "    \n",
    "    # Reshape\n",
    "    x = x.view(B, H_pad // window_size, window_size, W_pad // window_size, window_size, C)\n",
    "    windows = x.permute(0, 1, 3, 2, 4, 5).contiguous().view(-1, window_size, window_size, C)\n",
    "    return windows\n",
    "\n",
    "def window_reverse(windows, window_size, H, W):\n",
    "    # Determine B from H, W and window_size\n",
    "    B = int(windows.shape[0] / ((H + window_size - 1) // window_size * (W + window_size - 1) // window_size))\n",
    "    \n",
    "    # Pad if needed\n",
    "    pad_h = (window_size - H % window_size) % window_size\n",
    "    pad_w = (window_size - W % window_size) % window_size\n",
    "    \n",
    "    # Updated H, W after padding\n",
    "    H_pad, W_pad = H + pad_h, W + pad_w\n",
    "    \n",
    "    # Reshape\n",
    "    x = windows.view(B, H_pad // window_size, W_pad // window_size, window_size, window_size, -1)\n",
    "    x = x.permute(0, 1, 3, 2, 4, 5).contiguous().view(B, H_pad, W_pad, -1)\n",
    "    \n",
    "    # Remove padding if added\n",
    "    if pad_h > 0 or pad_w > 0:\n",
    "        x = x[:, :H, :W, :].contiguous()\n",
    "    \n",
    "    return x\n",
